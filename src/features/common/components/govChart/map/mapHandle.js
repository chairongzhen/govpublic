const fetchMapJson = url => {
  return fetch(url).then(response => {
    if (!response.ok) {
      return Promise.reject(response.status);
    }
    return response.json();
  });
};

export function handle(param, eChartInstance) {
  let name = param.name;
  if (
    name !== 'china' &&
    !(name in provincesMap) &&
    specialCity[name] === undefined &&
    specialCity[name] === null &&
    !(name in cityMap)
  ) {
    switch (name) {
      case '新疆':
        name = '新疆维吾尔自治区';
        break;
      case '广西':
        name = '广西壮族自治区';
        break;
      case '内蒙古':
        name = '内蒙古自治区';
        break;
      case '宁夏':
        name = '宁夏回族自治区';
        break;
      case '西藏':
        name = '西藏自治区';
        break;
      default:
        name += '省';
        break;
    }
  }

  let url;
  if (name in provincesMap) {
    url = `./mapJson/province/${provincesMap[name]}.json`;
  } else if (name in cityMap) {
    url = `./mapJson/city/${cityMap[name]}.json`;
  } else {
    console.warn(
      '%c 🥟 选择的地区无下级地图数据: ',
      'font-size:20px;background-color: #E41A6A;color:#fff;',
      name,
    );

    return;
  }

  fetchMapJson(url)
    .then(res => {
      registerMap(name, res);
      eChartInstance.setOption({
        series: [
          {
            type: 'map',
            map: name,
          },
        ],
      });
    })
    .catch(function(err) {
      console.warn(
        '%c 🥟 在服务端没有选择的地区的无下级地图数据: ',
        'font-size:20px;background-color: #E41A6A;color:#fff;',
        name,
      );
    });
}

let catchArray = [];
export default function(code) {
  let eCode = code2ECode[code];
  return new Promise((resolve, reject) => {
    if (catchArray.indexOf(eCode) !== -1) {
      resolve(eCode);
    } else {
      if (eCode) {
        fetchMapJson(`./mapJson/${eCode}.json`)
          .then(res => {
            registerMap(eCode, res);
            catchArray.push(eCode);
            resolve(eCode);
          })
          .catch(function(err) {
            console.warn(
              '%c 🥟 选择的地区无下级地图数据: ',
              'font-size:20px;background-color: #E41A6A;color:#fff;',
              code,
            );
            resolve('china');
          });
      } else {
        resolve('china');
      }
    }
  });
}
